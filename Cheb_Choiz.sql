CREATE DATABASE IF NOT EXISTS Cheb_Choiz;

USE Cheb_Choiz;

CREATE TABLE CATEGORY (
    CATE_ID NUMERIC(5) PRIMARY KEY,
    CATE_NAME VARCHAR(10) NOT NULL
);
ALTER TABLE CATEGORY
MODIFY CATE_NAME VARCHAR(50) NOT NULL;

CREATE TABLE PRICE (
    PRICE_ID NUMERIC(5) PRIMARY KEY,
    RANGE_NAME VARCHAR(10) NOT NULL,
    MIN_PRICE NUMERIC(6,2),
    MAX_PRICE NUMERIC(6,2)
);
ALTER TABLE PRICE
MODIFY RANGE_NAME VARCHAR(50) NOT NULL;

CREATE TABLE USER (
    USER_ID NUMERIC(5) PRIMARY KEY,
    USER_NAME VARCHAR(10) NOT NULL
);
ALTER TABLE USER
MODIFY USER_NAME VARCHAR(50) NOT NULL;

-- *** 2. แก้ไขตาราง MENU (ลบ CATE_ID และ Foreign Key) ***
CREATE TABLE MENU (
    MENU_ID NUMERIC(5) PRIMARY KEY,
    MENU_NAME VARCHAR(10) NOT NULL,
    MENU_PRICE NUMERIC(6,2) NOT NULL,
    -- CATE_ID ถูกลบออกไปจากตารางนี้
    PRICE_ID NUMERIC(5) NOT NULL,
    
    FOREIGN KEY (PRICE_ID) REFERENCES PRICE(PRICE_ID)
    -- FOREIGN KEY (CATE_ID) ถูกลบออกไปจากตารางนี้
);
ALTER TABLE MENU
MODIFY MENU_NAME VARCHAR(50) NOT NULL;

-- *** 3. เพิ่มตารางเชื่อม MENU_CATEGORY สำหรับความสัมพันธ์แบบ Many-to-Many ***
CREATE TABLE MENU_CATEGORY (
    MENU_ID NUMERIC(5) NOT NULL,
    CATE_ID NUMERIC(5) NOT NULL,
    
    PRIMARY KEY (MENU_ID, CATE_ID),
    FOREIGN KEY (MENU_ID) REFERENCES MENU(MENU_ID),
    FOREIGN KEY (CATE_ID) REFERENCES CATEGORY(CATE_ID)
);


CREATE TABLE SITUATION (
    SIT_ID NUMERIC(5) PRIMARY KEY,
    SIT_NAME VARCHAR(30) NOT NULL
);

CREATE TABLE MENU_SITUATION (
    MENU_ID NUMERIC(5) NOT NULL,
    SIT_ID NUMERIC(5) NOT NULL,
    
    PRIMARY KEY (MENU_ID, SIT_ID),
    FOREIGN KEY (MENU_ID) REFERENCES MENU(MENU_ID),
    FOREIGN KEY (SIT_ID) REFERENCES SITUATION(SIT_ID)
);

CREATE TABLE HISTORY (
    HISTORY_ID NUMERIC(5) PRIMARY KEY,
    USER_ID NUMERIC(5) NOT NULL,
    MENU_ID NUMERIC(5) NOT NULL,
    SELECTION_DATE DATETIME, 
    
    FOREIGN KEY (USER_ID) REFERENCES USER(USER_ID),
    FOREIGN KEY (MENU_ID) REFERENCES MENU(MENU_ID)
);

